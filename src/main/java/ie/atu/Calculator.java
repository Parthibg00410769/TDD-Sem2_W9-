package ie.atu;

public class Calculator {

    public int add(int a, int b) {
        return a + b;
    }

    public int subtract(int c, int d) {
        return c - d;
    }

    public int multiply(int e, int f) {
        return e * f;
    }

    public int divide(int g, int h) {
        return g / h;
    }
}